#include <LynxEngine_PCH.h>
#include <Animation/LynxAnimationKey.h>

namespace LynxEngine 
{	
	namespace Animation
	{
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAnimationKey::CAnimationKey(void)
		{			
			m_StartTime = 0.0f;
			m_EndTime = 0.0f;
			m_Values.clear();
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAnimationKey::CAnimationKey(int num)
		{			
			m_StartTime = 0.0f;
			m_EndTime = 0.0f;
			SetNumValues(num);
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAnimationKey::~CAnimationKey(void)
		{
			m_Values.clear();
		}			
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		void CAnimationKey::operator =(const CAnimationKey& rhs)
		{
			m_StartTime = rhs.m_StartTime;
			m_EndTime	= rhs.m_EndTime;		

			for (int i=0; i<rhs.m_Values.size(); i++)
			{
				m_Values[i] = rhs.m_Values[i];
			}
		}
	}
}	