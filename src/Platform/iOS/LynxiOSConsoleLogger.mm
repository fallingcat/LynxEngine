		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		void CConsoleLogger::vLog(const CString& str)
		{
			if (m_bEnable)
			{
				NSLog(@"%s", str.c_str());
			}
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		void CConsoleLogger::vLog(const LYNXCHAR* str)
		{
			if (m_bEnable)
				vLog(CString(str));
		}
	}
