#include <CollisionSystem/LynxAABB.h>

namespace LynxEngine 
{	
	namespace CollisionSystem 
	{
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAABB::CAABB(void) :
			m_Center(0.0f, 0.0f, 0.0f), 
			m_Extension(1.0f, 2.0f, 3.0f)
		{
			m_Type = AABB;				
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAABB::CAABB(CVector3& c, CVector3& e) :
			m_Center(c), 
			m_Extension(e)
		{
			m_Type = AABB;				
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAABB::CAABB(CVector3* p, int Num)			
		{
			Set(p, Num);
			m_Type = AABB;				
		}			
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CAABB::~CAABB(void)
		{
		}				
	}
}