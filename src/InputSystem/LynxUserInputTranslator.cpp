#include <LynxEngine_PCH.h>
#include <InputSystem/LynxUserInputTranslator.h>
#include <InputSystem/LynxInputDevice.h>

namespace LynxEngine 
{	
	namespace InputSystem 
	{
		//-------------------------------------------------------------------------------------------------------
		//
		//  :   
		//-------------------------------------------------------------------------------------------------------
		CUserInputTranslator::CUserInputTranslator(CInputDevice* lpd)
		{
			m_lpDevice = lpd;
			m_EventMap.clear();
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  :   
		//-------------------------------------------------------------------------------------------------------
		CUserInputTranslator::~CUserInputTranslator(void)
		{
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  :   
		//-------------------------------------------------------------------------------------------------------
		void CUserInputTranslator::RegisterEvent(int k, CEvent& e)
		{
			m_EventMap[k] = e;
		}
	}
}
