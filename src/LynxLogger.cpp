#include <LynxEngine_PCH.h>
#include <LynxLogger.h>
#include <LynxOutputdevice.h>

namespace LynxEngine 
{	
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	CLogger::CLogger(void)	
	{
		m_String.clear();
		m_lpOutputDevice = NULL;
		m_bEnable = LYNX_TRUE;
	}	
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	CLogger::~CLogger(void)
	{
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	void CLogger::vCreate(COutputDevice* out, const CString& name)
	{
		m_lpOutputDevice = out;
		if (m_lpOutputDevice)
		{
			m_lpOutputDevice->vOpen(name);
			m_lpOutputDevice->vAppend(m_String);
		}
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	void CLogger::vClearLog(void)
	{
		m_String.clear();
		if (m_lpOutputDevice)
			m_lpOutputDevice->vClear();
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	void CLogger::vLog(const CString& str)
	{
		if (m_bEnable)
		{
			m_String += str;
			if (m_lpOutputDevice)
				m_lpOutputDevice->vAppend(str);
		}
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	void CLogger::vLog(const LYNXCHAR* str)
	{
		if (m_bEnable)
			vLog(CString(str));
	}
}