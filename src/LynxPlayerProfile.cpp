#include <LynxEngine_PCH.h>
#include <LynxPlayerProfile.h>

namespace LynxEngine 
{	
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	CPlayerProfile::CPlayerProfile(const CString& filename)
	: CUserProfile(filename)
	{	
		m_Name = CString(_T("anonymous"));		
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	CPlayerProfile::~CPlayerProfile() 
	{		
	}	
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	LYNXBOOL CPlayerProfile::vLoad() 
	{		
		LYNXBOOL Result = LYNX_TRUE;
		CPlatformFileStream File;

		if (File.vOpen(m_Filename))
		{
			File.vClose();
			Result = CUserProfile::Load();

			m_Name = Read("Name", m_Name);			
		}
		else
		{			
			Save();
		}	
		return Result;
	}
	//-------------------------------------------------------------------------------------------------------
	//
	//  說明:   
	//-------------------------------------------------------------------------------------------------------
	void CPlayerProfile::vSave() 
	{			
		Write("Name", m_Name);		
		CUserProfile::Save();
	}	
}