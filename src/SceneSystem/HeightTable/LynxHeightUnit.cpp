#include <LynxEngine_PCH.h>
#include <SceneSystem/HeightTable/LynxHeightUnit.h>

namespace LynxEngine 
{
	namespace SceneSystem 
	{
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CHeightUnit::CHeightUnit()
		{
			m_NumHeight = 0;
			m_lpHeight = NULL;
		}	
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CHeightUnit::~CHeightUnit()
		{
			Release();
		}		
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		void CHeightUnit::Release()
		{
			if (m_lpHeight)
				delete [] m_lpHeight;
		}
	}
}