#include <LynxEngine_PCH.h>

#include <SoundSystem/LynxSoundSystem.h>
#include <LynxOSLBuffer.h>

namespace LynxSound 
{
	namespace OpenSL 
	{		
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CBuffer::CBuffer()
		{
			m_ID = NULL;
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CBuffer::~CBuffer()
		{
			if (m_ID)
			{
				LYNXDEL_ARRAY(m_ID);
				m_ID = NULL;
			}
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		LYNXBOOL CBuffer::Create(char* buffer)
		{
			m_ID = buffer;

			return LYNX_TRUE;
		}
	}
}