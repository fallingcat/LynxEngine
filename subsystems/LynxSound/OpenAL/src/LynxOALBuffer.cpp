#include <LynxEngine_PCH.h>

#include <SoundSystem/LynxSoundSystem.h>
#include <LynxOALBuffer.h>

namespace LynxSound 
{
	namespace OpenAL 
	{		
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CBuffer::CBuffer()
		{
			m_ID = 0;
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		CBuffer::~CBuffer()
		{
			if (m_ID)
			{
				alDeleteBuffers(1, &m_ID);
				m_ID = 0;
			}
		}
		//-------------------------------------------------------------------------------------------------------
		//
		//  說明:   
		//-------------------------------------------------------------------------------------------------------
		LYNXBOOL CBuffer::Create()
		{
			alGenBuffers(1, &m_ID);

			alGetError();
			if (alGetError() != AL_NO_ERROR)
				return LYNX_FALSE;

			return LYNX_TRUE;
		}
	}
}